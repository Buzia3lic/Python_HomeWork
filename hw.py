# Напишите программу, которая принимает на вход вещественное число и показывает сумму его цифр.
# Пример:
# - 6782 -> 23
# - 0,56 -> 11

if task == 1:
    print('Программа принимает на вход вещественное число и показывает сумму его цифр.')
    print('#################################################################################')
    num = input('Введите число: ')
    sum = 0
    i = 0
    while i < len(num):
        if num[i].isdigit():
            sum += int(num[i])
            i += 1
        else:
            i += 1
    print(sum)

