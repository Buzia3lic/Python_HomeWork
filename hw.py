    print('#################################################################################')
    num = int(input('Введите число: '))
    if num == 0:
        print('Так нельзя!')
    else:
        res = []
        for i in range(1, num + 1):
            res.append(round((1 + 1 / i) ** i, 2))
        print('Список элементов: ', res)
    sum = 0
    for i in range(len(res)):
        sum += res[i]
    print(f'Сумма элементов равна: {sum}')

